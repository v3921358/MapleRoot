function getPageList(itemIds) {
    var options = "";
    var rowCounter = 0;
    //
    var pages = (itemIds.length/20);
    for (var i = 0; i < pages; i++) {
        options += "#L" + i + "#Page " + (i+1)
        rowCounter++;
        if (rowCounter === 5) {
            options += "\r\n";
            rowCounter = 0;
        }
    }
    return options;
}

function getOptionsList(itemIds) {
    var options = "";
    var rowCounter = 0;
    
    if(split === true) {
        var startIndex = (pageNumber - 1) * 20;
        var endIndex = Math.min(startIndex + 20 - 1, itemIds.length - 1);

        for (var i = startIndex; i <= endIndex; i++) {
            var itemId = itemIds[i];
            options += "#L" + itemId + "#" + "#v" + itemId + ":#" + "  ";
            rowCounter++;
            if (rowCounter === 5) {
                options += "\r\n";
                rowCounter = 0;
            }
        }
        if(pageNumber > 1) {
            options += "#L1#Previous Page"
        }
        if(pageNumber <= itemIds.length/20) {
            options += "\r\n#L0#Next Page"
        }
    } else {
        for (var i = 0; i < itemIds.length; i++) {
            var itemId = itemIds[i];
            options += "#L" + itemId + "#" + "#v" + itemId + ":#" + "  ";
            rowCounter++;
            
            if (rowCounter === 5) {
                options += "\r\n";
                rowCounter = 0;
            }
        }        
    }
    return options;
}

