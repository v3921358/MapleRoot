function action(mode, type, selection) {
    if (mode == -1) {
        cm.dispose();
    } else {
        if (mode == 0) {
            cm.dispose();
            return;
        }
        if (mode == 1)
            status++;
        else
            status--;

        switch (status) {
            case 0:
                showMainOptions();
                break;
            case 1:
                handleMainSelection(selection);
                break;
            case 2:
                handleSubSelection(selection);
                break;
            case 3:
                handlePageSelection(selection);
                break;
            case 4:
                confirmPurchase(selection);
                break;
            case 5:
                completePurchase(mode, selection);
                break;
        }
    }
}

